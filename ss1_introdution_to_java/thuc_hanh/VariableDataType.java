package ss1_introdution_to_java.thuc_hanh;

public class VariableDataType {
    public static void main(String[] args) {
        int i = 10;
        float j = 10.5f;
        double k = 10.5d;
        boolean g = true;
        char h = 'a';
        String f = "Le Tu Khoa";
        System.out.println("i la: " + i);
        System.out.println("j la: " + j);
        System.out.println("k la: " + k);
        System.out.println("g la: " + g);
        System.out.println("h la: " + h);
        System.out.println("f la: " + f);
    }
}
